LRESULT CMainDlg::OnInitDialog(UINT /*uMsg*/, WPARAM /*wParam*/, LPARAM /*lParam*/, BOOL& /*bHandled*/)
{
	// center the dialog on the screen
	CenterWindow();

	// set icons
	HICON hIcon = (HICON)::LoadImage(_Module.GetResourceInstance(), MAKEINTRESOURCE(IDR_MAINFRAME), 
		IMAGE_ICON, ::GetSystemMetrics(SM_CXICON), ::GetSystemMetrics(SM_CYICON), LR_DEFAULTCOLOR);
	SetIcon(hIcon, TRUE);
	HICON hIconSmall = (HICON)::LoadImage(_Module.GetResourceInstance(), MAKEINTRESOURCE(IDR_MAINFRAME), 
		IMAGE_ICON, ::GetSystemMetrics(SM_CXSMICON), ::GetSystemMetrics(SM_CYSMICON), LR_DEFAULTCOLOR);
	SetIcon(hIconSmall, FALSE);

	m_HtmlCtrl.SubclassWindow( GetDlgItem( IDC_CTL_HTMLLIST ) );
	m_HtmlCtrl.SetExtendedStyle( HTMLLIST_STYLE_GRIDLINES|HTMLLIST_STYLE_CHECKBOX );
	m_HtmlCtrl.InsertItem( _T("<p>Original code from: <b>Manoj Subberwal</b><br>Copyright (c) 2006</p>"));
	m_HtmlCtrl.InsertItem( _T("<p>Hello my name is <b>Ernest Laurentin</b>.<br>Thanks for using WTL version this HTML ListView</p>"));
	m_HtmlCtrl.InsertItem( _T("<p>Codeproject is nice place indeed:<br><br><b><u>http://www.codeproject.com/</u></b></p>"));
	return TRUE;
}

